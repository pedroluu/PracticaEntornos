package clasetres;

public class Conserje {
	
	private String nombre;
	private int edad;
	public Conserje() {
		super();
	}
	public String getNombre() {
		return nombre;
	}
	public int getEdad() {
		return edad;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	
	
	
}
